void drawPoint(uint16_t x, uint16_t y, uint16_t color){
	LCD_SetXY(x,y);
	LCD_WriteReg(0x2c, color);
}

void clearScreen(uint16_t color){
	LCD_SetXY(0,0);
	LCD_WR_REG(0x2c);
	for(int i=0;i<(240*320);i++){
		LCD_WR_DATA(color);
	}
}

